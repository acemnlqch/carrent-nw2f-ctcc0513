    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                String cust_ID= textCar.getText();
                String name = textMake.getText();      
                String address = textAddress.getText();
                String MobileNo = textMobile.getText();
                
                
                // Check if all fields are filled before proceeding
                if (cust_ID.isEmpty() || name.isEmpty() || address.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Please fill all fields before adding a customer.");
                    return;
                }

                try {
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    con = DriverManager.getConnection("jdbc:mysql://localhost/carrent", "root", "");
                    pst = con.prepareStatement("insert into customer(cust_id,name,address,mobile_no) values(?,?,?,?)");
                    
                    pst.setString(1, cust_ID);
                    pst.setString(2, name);
                    pst.setString(3, address);
                    pst.setString(4, MobileNo);
                    pst.executeUpdate();
                    
                    JOptionPane.showMessageDialog(null, "Customer Added....");
                    setVisible(true);
                    textMake.setText("");
                    textAddress.setText("");  // Clear the JTextArea
                    textMobile.setText("");
                    textMake.requestFocus();
                    autoID();
                    table_update();

                } catch (ClassNotFoundException | SQLException ex) {
                    ex.printStackTrace();
                }
            
    }//GEN-LAST:event_jButton1ActionPerformed

