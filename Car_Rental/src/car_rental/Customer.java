    public void table_update(){
        int c;
    	
    	try {
		Class.forName("com.mysql.cj.jdbc.Driver");
	        con = DriverManager.getConnection("jdbc:mysql://localhost/carrent", "root", "");
	        pst = con.prepareStatement("select * FROM customer");
	        ResultSet rs = pst.executeQuery();
	        
	        ResultSetMetaData rd = rs.getMetaData();
	        c = rd.getColumnCount();
	        DefaultTableModel df =(DefaultTableModel)table.getModel();
	        df.setRowCount(0);
	        
	        while(rs.next()) {
	        	Vector vector = new Vector();
	        	
	        	for(int i = 1; i<=c; i++) {
	        		
	        		vector.add(rs.getString("cust_id"));
	        		vector.add(rs.getString("name"));
	        		vector.add(rs.getString("address"));
	        		vector.add(rs.getString("mobile_no"));

	        	}
	        	df.addRow(vector);
	        }

		} catch (ClassNotFoundException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
        }
    public void autoID(){
        try {
            // Ensure the connection and query are performed after components are initialized
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/carrent", "root", "");
            Statement s = con.createStatement();

            // SQL query to fetch the max car number
            ResultSet rs = s.executeQuery("SELECT MAX(cust_id) FROM customer");
            rs.next();

            String maxCarNo = rs.getString(1);  // Get the value of MAX(car_no)

            // Handle null case and increment the car number
            if (maxCarNo == null) {
                textCar.setText("A0001");
            } else {
                long id = Long.parseLong(maxCarNo.substring(1));
                id++;
                textCar.setText("A" + String.format("%04d", id)); 
            }
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    
